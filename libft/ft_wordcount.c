#include "libft.h"

size_t	ft_wordcount(char const *s, char c)
{
	size_t	count;

	count = 0;
	while (*s)
	{
		while (*s && *s == c)
			s++;
		while (*s && *s != c)
		{
			if (*(s + 1) == c || *(s + 1) == '\0')
				count++;
			s++;
		}
	}
	return (count);
}
